const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const ROOT_DIR = path.join(__dirname, "..");
const TIMELINE_DIR = path.join(ROOT_DIR, "PROJECT_MEMORY", "29_TIMELINE");

if (!fs.existsSync(TIMELINE_DIR)) {
  fs.mkdirSync(TIMELINE_DIR, { recursive: true });
}

try {
  // Use powershell syntax for git log to safely get format
  const log = execSync('git log --pretty=format:"| %h | %ad | %s |" --date=short').toString();

  const content = `# Git Architecture & Feature Timeline

This timeline is generated entirely from the raw Git repository history. No events are manually fabricated.

| Commit Hash | Date | Message |
| ----------- | ---- | ------- |
${log}

_Generated by mine-git-history.cjs_
`;

  fs.writeFileSync(path.join(TIMELINE_DIR, "architecture-timeline.md"), content);
  console.log("Git history mined successfully.");
} catch (e) {
  console.log("Failed to mine git history. Not a git repository?");
}
